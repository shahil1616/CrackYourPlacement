class Solution {
    public boolean lemonadeChange(int[] bills) {
        int amount=0;
        int f5=0;
        int t10=0;
        int t20=0;
        for(int i=0;i<bills.length;i++)
        {
            if(bills[i]==5)
            f5++;
            else if(bills[i]==10)
            {
                if(f5<=0)
                {
                    return false;
                }
                else
                {
                    f5--;
                    t10++;
                }
            }
            else if(bills[i]==20)
            {
                if(t10==0)
                {
                    if(f5<=2)
                    {
                        return false;
                    }
                    else
                    {
                        f5=f5-3;
                    }
                }
                else
                {
                    if(f5<=0)
                    {
                        return false;
                    }
                    else
                    {
                        t10--;
                        f5--;
                    }

                }
            }

        }
        return true;
    }
}















































































































































































































































































































































































































































































































































































































































































































































        
        
 